package co.edu.usbcali.dto;

public class MissionLevelDTO {
	
	private int idMissionLevel;
	private String nameMissionLevel;
	private String imgMissionLevel;
	private Integer scoreMissionLevel;
	
	public int getIdMissionLevel() {
		return idMissionLevel;
	}
	public void setIdMissionLevel(int idMissionLevel) {
		this.idMissionLevel = idMissionLevel;
	}
	public String getNameMissionLevel() {
		return nameMissionLevel;
	}
	public void setNameMissionLevel(String nameMissionLevel) {
		this.nameMissionLevel = nameMissionLevel;
	}
	public String getImgMissionLevel() {
		return imgMissionLevel;
	}
	public void setImgMissionLevel(String imgMissionLevel) {
		this.imgMissionLevel = imgMissionLevel;
	}
	public Integer getScoreMissionLevel() {
		return scoreMissionLevel;
	}
	public void setScoreMissionLevel(Integer scoreMissionLevel) {
		this.scoreMissionLevel = scoreMissionLevel;
	}
	
}
